#include <iostream>
#include <vector>
using namespace std;
bool hasArrayTwoCandidates(int arr[], int n, int x) {
    // code here
    bool isPair = false;
    sort(arr, arr + n);
    int start = 0;
    int end = n - 1;

    while (start < end) {
        int sum = arr[start] + arr[end];
        if (sum == x)
            return true;
        else if (sum < x)
            start++;
        else
            end--;
    }
    return isPair;
}